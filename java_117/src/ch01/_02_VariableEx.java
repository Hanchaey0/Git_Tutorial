package ch01;

public class _02_VariableEx {
	public static void main (String [] args) {
		//연산
		int num1=  40;
		int num2= 20;
		
		//60=40+20;
		int addResult = num1+num2;
		System.out.println("두 수의 합 : " + addResult);
		
		int subResult = num1 - num2;
		System.out.println("두 수의 차 : " + subResult);
		
		int mulResult = num1*num2;
		System.out.println("두 수의 곱 : " + mulResult);
		
		int divResult = num1 / num2;
		System.out.println("두 수의 나눔 : " +divResult);
				
		
		
	}
}
